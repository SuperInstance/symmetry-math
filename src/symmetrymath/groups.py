def _perm_compose(p: tuple[int, ...], q: tuple[int, ...]) -> tuple[int, ...]:
    """Compose two permutations (right-to-left convention: (p∘q)(i) = p[q[i]])."""
    return tuple(p[q[i]] for i in range(len(p)))


def _perm_inverse(p: tuple[int, ...]) -> tuple[int, ...]:
    inv = [0] * len(p)
    for i, v in enumerate(p):
        inv[v] = i
    return tuple(inv)


class SymmetricGroup(Group):
    """The symmetric group *S_n* — all permutations of *n* objects.

    Elements are represented as tuples ``(σ(0), σ(1), …, σ(n-1))``.
    """

    def __init__(self, n: int) -> None:
        if n < 1:
            raise ValueError("n must be ≥ 1")
        self._n = n
        self._elems: frozenset[tuple[int, ...]] = frozenset(self._all_perms())

    def _all_perms(self) -> Iterator[tuple[int, ...]]:
        from itertools import permutations
        for p in permutations(range(self._n)):
            yield p

    @property
    def elements(self) -> FrozenSet[Hashable]:
        return self._elems  # type: ignore[return-value]

    def operation(self, a: Hashable, b: Hashable) -> Hashable:
        return _perm_compose(a, b)  # type: ignore[arg-type]

    @property
    def identity(self) -> Hashable:
        return tuple(range(self._n))

    def inverse(self, a: Hashable) -> Hashable:
        return _perm_inverse(a)  # type: ignore[arg-type]

    def __repr__(self) -> str:
        return f"S_{self._n}"


class CyclicGroup(Group):
    """The cyclic group *C_n* = ℤ/nℤ under addition mod *n*.

    Elements are integers ``0, 1, …, n-1``.
    """

    def __init__(self, n: int) -> None:
        if n < 1:
            raise ValueError("n must be ≥ 1")
        self._n = n
        self._elems: frozenset[int] = frozenset(range(n))

    @property
    def elements(self) -> FrozenSet[Hashable]:
        return self._elems  # type: ignore[return-value]

    def operation(self, a: Hashable, b: Hashable) -> Hashable:
        return (int(a) + int(b)) % self._n  # type: ignore[return-value]

    @property
    def identity(self) -> Hashable:
        return 0

    def inverse(self, a: Hashable) -> Hashable:
        return (-int(a)) % self._n  # type: ignore[return-value]

    def is_abelian(self) -> bool:
        return True

    def __repr__(self) -> str:
        return f"C_{self._n}"


class DihedralGroup(Group):
    """The dihedral group *D_n* — symmetries of a regular *n*-gon.

    Elements are pairs ``(k, flip)`` where ``k ∈ {0,…,n-1}`` is the
    rotation component and ``flip ∈ {0, 1}`` indicates reflection.
    """

    def __init__(self, n: int) -> None:
        if n < 3:
            raise ValueError("n must be ≥ 3 for a polygon")
        self._n = n
        self._elems: frozenset[tuple[int, int]] = frozenset(
            (k, f) for k in range(n) for f in (0, 1)
        )

    @property
    def elements(self) -> FrozenSet[Hashable]:
        return self._elems  # type: ignore[return-value]

    def operation(self, a: Hashable, b: Hashable) -> Hashable:
        k1, f1 = a  # type: ignore[misc]
        k2, f2 = b  # type: ignore[misc]
        # (k1, f1) ∘ (k2, f2) = (k1 + (-1)^f1 * k2 mod n, f1⊕f2)
        if f1 == 0:
            k = (k1 + k2) % self._n
        else:
            k = (k1 - k2) % self._n
        return (k, f1 ^ f2)

    @property
    def identity(self) -> Hashable:
        return (0, 0)

    def inverse(self, a: Hashable) -> Hashable:
        k, f = a  # type: ignore[misc]
        if f == 0:
            return ((-k) % self._n, 0)
        return (k, 1)

    def __repr__(self) -> str:
        return f"D_{self._n}"
